public class Starecomanda {
    private int id;
    public String tip;

    public Starecomanda(int id, String tip)
    {
        this.id = id;
        this.tip = tip;
    }

    public void setId(int id)
    {
        this.id = id;
    }

    public String getTip()
    {
        return tip;
    }
}
